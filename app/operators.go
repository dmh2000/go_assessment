package goassessment

// write a function that uses the addition operator
func add(a int, b int) int {
	return a + b
}

// write a function that uses the subtraction operator
func subtract(a int, b int) int {
	return a - b
}

// write a function that uses the multiplication operator
func multiply(a int, b int) int {
	return a * b
}

// write a function that uses the division operator
func divide(a int, b int) int {
	return a / b
}

// write a function that uses the remainder (integer modulo) operator
func remainder(a int, b int) int {
	return a % b
}

// write a function that uses the equal operator
func equal(a int, b int) bool {
	return a == b
}

// write a function that uses the not equal operator
func notEqual(a int, b int) bool {
	return a != b
}

// write a function that uses the less-than operator
func lessThan(a int, b int) bool {
	return a < b
}

// write a function that uses the less-than or equal operator
func lessThanEqual(a int, b int) bool {
	return a <= b
}

// write a function that uses the greater-than operator
func greaterThan(a int, b int) bool {
	return a > b
}

// write a function that uses the greater-than or equal operator
func greaterThanEqual(a int, b int) bool {
	return a >= b
}

// write a function that returns true if either argument is true
func either(a bool, b bool) bool {
	return a || b
}

// write a function that returns true only if both arguments are true
func both(a bool, b bool) bool {
	return a && b
}

// write a function that returns true only if both arguments are false
func none(a bool, b bool) bool {
	return !a && !b
}
